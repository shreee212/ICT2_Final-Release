      <h2 style="text-align: center; font-size: 200%; color: red;"><b>Where the Year Has Gone By?</b></h2><br>
      <h5 style="font-size: 150%;"><b>Recent changes in visa by the Australian Government , Dec 7, 2020</b></h5>
      <!--<div class="fakeimg" style="height:200px;">Image</div>-->
      <p style="color: black; font-size: 140%">“It’s December already, where has the year gone to?”
        is a common comment at this time of year but this particular year we know
        where we spent much of our time – isolated in our homes. It’s a year demarcated
        by isolation and stagnation from an economic and social perspective and
        next year can only be better. This is also true in terms of immigration
        policy which left very little to cheer about in 2020 but the outlook for
        2021 has to be brighter.<br>

        <br>In terms of the year that’s been, things turned sour fairly early on with Australia
        calling Covid 19 a pandemic prior to the announcement by the World Trade Organisation
        and essentially closed its borders on 20 March 2020 with exemptions for only
        Australian citizens, permanent residence and their immediate family.<br>

        <br>In May we would normally receive the Federal Government’s budget announcement
        including details relating to the annual migration quota however this decision
        was postponed until October. This delay is why state sponsorship lists, which are
        normally produced in July were postponed until such time as the government finalised
        the migration quota so that they could decide on the number of migrants each state
        could sponsor.<br>

        <br>Many of our clients overseas applying for general skilled migration visas are
        dependent upon the production of state sponsorship lists but as I write this these
        state allocations have yet to be finalised. All indications are that it should happen
        soon and we expect state sponsorship list to be produced later this month.<br>

        <br>Delays associated with state sponsorship lists have meant that many of our clients
        who had obtained English-language tests and positive skills assessment have had
        to essentially tread water for much of this year. Even those offshore applicants
        who had filed general skilled migration visa applications have had to wait as
        priority was given to those already in Australia. With 36,000 Australians stranded
        overseas the government hardly wanted to add to the number of permanent residents
        overseas wanting to gain access to Australia by approving offshore cases hence the
        focus on processing applications made while the candidate was in Australia.<br>

        <br>I’m sure that 2021 will however be a brighter year in terms of migration to
        Australia. There are several Covid 19 vaccines in production and Australia has entered
        into five separate agreements for the supply of Covid 19 vaccines if they are proven to
        be safe and effective.<br>

        <br>Our interstate borders have been relaxed and it is now possible to travel
        anywhere in Australia except Western Australia and that’s set to change next week.
        In terms of our national border Australia already has quarantine-free travel arrangements
        with New Zealand and is working towards establishing similar arrangements with other
        low-risk countries such as Japan, Singapore and South Korea.<br>

        <br>Towards the middle of next year, we should see a gradual return of some of
        the 120,000 international students who remain stranded overseas and of course July
        of next year demarcates the beginning of another immigration year with the publication
        of more state sponsorship lists.<br>

        <br>The number and variety of occupations appearing on state sponsorship lists usually
        mirror economic activity in a particular state and relevantly the federal government is
        providing $257 billion in direct economic support to cushion the blow and strengthen
        the recovery. The 2020 – 21 budget commits a further $98 billion including $25 billion
        in direct Covid 19 response measures and $74 billion in new measures to create jobs
        bringing the government’s overall support to $507 billion. Other stimulatory measures such as tax
        reform, quantitative easing, and record low interest rates will no doubt have
        the desired effect of stimulating the Australian economy and, as the economy
        grows, so too do skill shortages and the need to import skills from overseas. <br>

        <br>Of course there is also the stimulatory measures of state governments to be
        added to Federal expenditure with Victoria alone spending $13 billion to combat
        the coronavirus crisis with more than $7.6 billion in direct economic support for
        business in Victoria.<br>

        <br>It’s probably going to be several years before we see the high level of tourists,
        students and migrants returning to Australia and it is conceivable that those
        allowed to enter might have to have a health passport (no jab no entry) in addition
        to a national passport to gain entry to Australia but there is a general feeling
        of optimism in Australia that the worst is behind us.<br>

        <br>Our message to those who feel that they have been treading water as far as
        migration to Australia is concerned is to hang in there. To those that have already
        filed applications that are as yet undecided we should see more decisions being made
        next year and to those who have completed English-language and skills assessments
        and are waiting on the state sponsorship lists, progress cannot be too far off with
        state sponsorship lists due to open up shortly as well as more updates in July
        of next year.<br>

        <br>If 1992 was an “annus horribilis” according to Queen Elizabeth II, I wonder
        how she would describe 2020? Whilst I’m not going to predict 2021 to be annus
        mirabilis (wonderful year) it has to be an improvement on 2020 from a health,
        social, economic and migration perspective.</p><br>

