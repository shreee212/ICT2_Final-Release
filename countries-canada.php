    <div class="centre">
        <h1 style="font-size:40px;"><b><span class="text-primary"><u>PROVINCIAL NOMINEE PROGRAM</u></span></b></h1>
    </div>
    <br>
    <p style="font-size: 25px; color: #000802;">The Provincial Nominee Programs (PNPs) allow Canadian provinces and
      territories to nominate individuals who wish to immigrate to Canada and
      who are interested in settling in a particular province</p><br>
    <p style="font-size: 25px; color: #000802;">Each Canadian province and territory (except Nunavut and Quebec) has
      its own unique Provincial Nominee Program. Each PNP has at least one
      immigration stream that is aligned with the federal Express Entry
      immigration selection system. </p><br>

    <p style="font-size: 25px; color: #000802;">Provincial nominations issued under these Express Entry-aligned streams
      are known as ‘enhanced nominations’ and award Express Entry candidates an
      additional 600 Comprehensive Ranking System(CRS) points.</p>
